package EstruturaCondicional;

public class OperadoresComparativos {
    public static void main(String[] args) {

        int a = 10;
        int b = 5;


        // maior
        System.out.println(a > b);

        //menor
        System.out.println(a < b);

        //maior ou igual
        System.out.println(a >= b);

        //menor ou igual
        System.out.println(a <= b);

        //igual
        System.out.println(a == b);

        //diferente
        System.out.println(a != b);


    }
}
