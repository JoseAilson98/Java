package EstruturaSequencial;

public class OperadorAritmeticos {
    public static void main(String[] args) {

        /*
        + Adição
        - Subtração
        * Multiplicação
        / Divisão
        % Resto da divisão (MOD)
         */

       int numero = 10;
       int numero2 = 20;
       int soma;

        //Adição
        soma = numero + numero2;
        System.out.println("Resultado: "+soma);

        //Subtração
        soma = numero - numero2;
        System.out.println("Resultado: "+soma);

        //Multiplicação
        soma = numero * numero2;
        System.out.println("Resultado: "+soma);

        //Divisão
        soma = numero / numero2;
        System.out.println("Resultado: "+soma);

        //Resto da divisão
        soma = numero * numero2;
        System.out.println("Resultado: "+soma);


    }
}
